    private void selectData() {
    String kolom[] = {"NmPetugas","noresi","nopol","jenis","tglmasuk","tglkeluar","harga"};
        DefaultTableModel dtm = new DefaultTableModel(null,kolom);
        String SQL = "SELECT * FROM tb_main";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try {
            while(rs.next()){
                String NmPetugas = rs.getString(1);
                String noresi = rs.getString(2);
                String nopol = rs.getString(3);
                String jenis = "";
                if("Mobil".equals(rs.getString(4))){
                    jenis = "Mobil";
                }else{
                    jenis = "Motor";
                }
                String masuk = rs.getString(5);
                String keluar = rs.getString(6);
                String harga = rs.getString(7);
               // String harga = rs.getString(7);
                String data[] = {NmPetugas,noresi,nopol,jenis,masuk,keluar,harga};
                dtm.addRow(data);
            }
        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(main_frame_uts.class.getName()).log(Level.SEVERE, null, ex);
        }
        jTable2.setModel(dtm);    }
