    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
       SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
       String masuk = dateFormat.format(tglmasuk.getDate());
       String keluar = dateFormat.format(tglkeluar.getDate());
       
       SimpleDateFormat dateFormat2 = new SimpleDateFormat("dd");
       String masuk2 = dateFormat2.format(tglmasuk.getDate());
       String keluar2 = dateFormat2.format(tglkeluar.getDate());
       String JK = "";
       if(rbmobil.isSelected()){
           JK = "Mobil";
       } else {
           JK="Motor";
       }
       //harga.setText()= keluar masuk;
       
       
       int masuk21 = Integer.parseInt(masuk2);
       int keluar21 = Integer.parseInt(keluar2);
       
       int hargaaa = keluar21-masuk21;

       int hargabaru = hargaaa * 3000;
       
       int hargaa = 0;
       if(nm_ptgs.getText().equals("") || noresi.getText().equals("") || nopol.getText().equals("") 
                || JK.equals("") || masuk.equals("")
                || keluar.equals("") ){
            JOptionPane.showMessageDialog(this, "Harap lengkapi data Anda", "Error", JOptionPane.WARNING_MESSAGE);
       }
       else{           
            String SQL = "INSERT INTO tb_main (NmPetugas,noresi,nopol,jenis,tglmasuk,tglkeluar,harga)"
                   +"VALUES('"+nm_ptgs.getText()
                   +"','"+noresi.getText()
                   +"','"+nopol.getText()
                   +"','"+JK+"','"+masuk+"','"+keluar+"','"+hargabaru+"')";
            
            int status = KoneksiDB.execute(SQL);
           
           if(status == 1){
               JOptionPane.showMessageDialog(this,"Data Berhasil Ditambahkan","Sukses",JOptionPane.INFORMATION_MESSAGE);
               selectData();
           }else{
               JOptionPane.showMessageDialog(this, "Data Gagal Ditambahkan","Gagal",JOptionPane.WARNING_MESSAGE);
           }
       }
       
        String a = Integer.toString(hargabaru);
        harga.setText(a);
       
    }//GEN-LAST:event_saveActionPerformed

